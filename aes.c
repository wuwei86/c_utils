void shift_left(unsigned char* arr) {
    unsigned char temp;
    temp = arr[0];
    arr[0] = arr[1];
    arr[1] = arr[2];
    arr[2] = arr[3];
    arr[3] = temp;
}

void shift_right(unsigned char* arr) {
    unsigned char temp;
    temp = arr[3];
    arr[3] = arr[2];
    arr[2] = arr[1];
    arr[1] = arr[0];
    arr[0] = temp;
}

void sub_bytes(unsigned char* arr, int len) {
    for(int i = 0; i < len; i++) {
        arr[i] = sbox[ arr[i] ];
    }
}

void expand_key(unsigned char key[16]) {
    int w;
	int i = 0;
    for(w = 0; w < 4; w++) {
        for(i = 0; i < 4; i++) {
            round_keys[w * 4 + i] = key[w * 4 + i];
        }
    }
    for(; w < 4 * (10 + 1); w++) {
        for(int i = 0; i < 4; i++) {
            round_keys[w * 4 + i] = round_keys[(w - 1) * 4 + i];
        }
        if(w % 4 == 0) {
            shift_left(&round_keys[w * 4]);
            sub_bytes(&round_keys[w * 4], 4);
            round_keys[w * 4] = round_keys[w * 4] ^ Rcon[w / 4];
        }
        for(i = 0; i < 4; i++) {
            round_keys[w * 4 + i] = round_keys[w * 4 + i] ^ round_keys[(w - 4) * 4 + i];
        }
    }
}

void create_state(unsigned char pt[16]) {
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            state[i][j] = pt[i + j * 4];
        }
    }
}

void shift_rows() {
    shift_left(&state[1][0]);

    shift_left(&state[2][0]);
    shift_left(&state[2][0]);

    shift_left(&state[3][0]);
    shift_left(&state[3][0]);
    shift_left(&state[3][0]);
}

void inverse_shift_rows() {
    shift_right(&state[1][0]);

    shift_right(&state[2][0]);
    shift_right(&state[2][0]);

    shift_right(&state[3][0]);
    shift_right(&state[3][0]);
    shift_right(&state[3][0]);
}

void mix_cols() {
    unsigned char a, b, c, d;
    int val;
    for(int col = 0; col < 4; col++) {
        a = state[0][col];
        b = state[1][col];
        c = state[2][col];
        d = state[3][col];
        state[0][col] = mul(a, 0x02) ^ mul(b, 0x03) ^ mul(c, 0x01) ^ mul(d, 0x01);
        state[1][col] = mul(a, 0x01) ^ mul(b, 0x02) ^ mul(c, 0x03) ^ mul(d, 0x01);
        state[2][col] = mul(a, 0x01) ^ mul(b, 0x01) ^ mul(c, 0x02) ^ mul(d, 0x03);
        state[3][col] = mul(a, 0x03) ^ mul(b, 0x01) ^ mul(c, 0x01) ^ mul(d, 0x02);
    }

}

void inverse_mix_cols() {
    unsigned char a, b, c, d;
    for(int col = 0; col < 4; col++) {
        a = state[0][col];
        b = state[1][col];
        c = state[2][col];
        d = state[3][col];
        state[0][col] = mul(a, 0x0e) ^ mul(b, 0x0b) ^ mul(c, 0x0d) ^ mul(d, 0x09);
        state[1][col] = mul(a, 0x09) ^ mul(b, 0x0e) ^ mul(c, 0x0b) ^ mul(d, 0x0d);
        state[2][col] = mul(a, 0x0d) ^ mul(b, 0x09) ^ mul(c, 0x0e) ^ mul(d, 0x0b);
        state[3][col] = mul(a, 0x0b) ^ mul(b, 0x0d) ^ mul(c, 0x09) ^ mul(d, 0x0e);
    }
}

void add_round_key(int r) {
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            state[i][j] ^= round_keys[16 * r + j * 4 + i];
        }
    }
}

void sub_bytes_void() {
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            state[i][j] = sbox[state[i][j]];
        }
    }
}

void inverse_sub_bytes() {
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            state[i][j] = inverse_sbox[ state[i][j] ];
        }
    }
}

void inverse_cipher() {
    add_round_key(10);

    for(int r = 9; r > 0; r--) {
        inverse_shift_rows();
        inverse_sub_bytes();
        add_round_key(r);
        inverse_mix_cols();

    }
    inverse_shift_rows();
    inverse_sub_bytes();

    add_round_key(0);
}


void cipher() {
    add_round_key(0);
    for(int r = 1; r < 10; r++) {
        sub_bytes_void();
        shift_rows();
        mix_cols();
        add_round_key(r);
    }
    sub_bytes_void();
    shift_rows();
    add_round_key(10);
}

void AES_encrypt(unsigned char plain_text[16], unsigned char key[16], unsigned char out[16]) {
    create_state(plain_text);
    expand_key(key);
    cipher();
    int i, j;
    for(i = 0; i < 4; i++) {
        for(j = 0; j < 4; j++) {
            out[i * 4 + j] = state[j][i];
        }
    }
}

void AES_decrypt(unsigned char cipher_text[16], unsigned char key[16], unsigned char out[16]) {
    create_state(cipher_text);
    expand_key(key);
    inverse_cipher();
    int i, j;
    for(i = 0; i < 4; i++) {
        for(j = 0; j < 4; j++) {
            out[i * 4 + j] = state[j][i];
        }
    }
}

